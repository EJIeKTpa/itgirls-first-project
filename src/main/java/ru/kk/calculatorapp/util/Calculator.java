package ru.kk.calculatorapp.util;


public class Calculator {

    public static int Sum(int Num1, int Num2) {
        return Num1 + Num2;
    }

    public static int Diff(int Num1, int Num2) {
        return Num1 - Num2;
    }

    public static int Comp(int Num1, int Num2) {
        return Num1 * Num2;
    }
}
